package com.lumtec.foliadorpdf.modelo;

public class Coordenadas {
    private float x;
    private float y;

    public Coordenadas(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
