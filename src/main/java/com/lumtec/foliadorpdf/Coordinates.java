package com.lumtec.foliadorpdf;

public class Coordinates {

    private float x, y;

    public Coordinates(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getCoordenadaX() {
        return this.x;
    }

    public float getCoordenadaY() {
        return this.y;
    }
}
